class Smartphone:
    def __init__(self, brand, model, sub_number):
        self.brand = brand
        self.model = model
        self.sub_number = sub_number
    
    def get_brand(self):
        return self.brand
    
    def get_model(self):
        return self.model
    
    def get_sub_number(self):
        return self.sub_number
